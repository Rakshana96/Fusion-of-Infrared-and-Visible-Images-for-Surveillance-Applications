set(handles.edit1,'String','');

set(handles.edit2,'String','');
set(handles.edit3,'String','');
set(handles.edit4,'String','');
set(handles.edit5,'String','');
set(handles.edit6,'String','');

global k1
global angle siz
siz = 256;
angle = 0;
k1 = handles;
load net
load cnn_net
clc;
[f,p]=uigetfile('databse\CHASEDB1\*','Load Input Image');

fm = MaskExtract(p,f);
fm = [p,fm];
fi = [p,f];
set(handles.edit1,'String',f);

vOtype_1 = get(handles.radiobutton1,'Value');
vOtype_2 = get(handles.radiobutton2,'Value');
vOtype_3 = get(handles.radiobutton3,'Value');
vOtype_4 = get(handles.radiobutton4,'Value');
if(vOtype_1)
    Eval = mainTestANN(fi,fm,net,handles);
elseif(vOtype_2)
    Eval = mainTestKmeans(fi,fm,handles);
elseif(vOtype_3)
    Eval = mainTestCNN(fi,fm,cnn_net,handles);
elseif(vOtype_4)
    Eval = mainTestProp(fi,fm,handles);
end

set(handles.edit2,'String',num2str(Eval(1)));
set(handles.edit3,'String',num2str(Eval(2)));
set(handles.edit4,'String',num2str(Eval(3)));
set(handles.edit5,'String',num2str(Eval(4)));
set(handles.edit6,'String',num2str(Eval(5)));


